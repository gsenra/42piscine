int		ft_isnumber(char valor[])
{
	int i;

	i = 0;
	while (valor[i] != '\0')
	{
		if (valor[i] < 48 || valor[i] > 57)
			return (1);
		i++;
	}
	return (0);
}

int		ft_strcmp(char *s1, char *s2)
{
	while ((*s1 != 0) && (*s2 != 0) && (*s1 == *s2))
	{
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}
