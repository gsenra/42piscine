void	ft_rev_int_tab(int *tab, int size)
{
	int a;
	int z;
	int bat;

	a = 0;
	z = size - 1;
	while (a < (size / 2))
	{
		bat = tab[a];
		tab[a] = tab[z];
		tab[z] = bat;
		a++;
		z--;
	}
}
